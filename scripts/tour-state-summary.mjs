// What does "all tours" mean? Count by status + check for booking dependencies
import mysql from "mysql2/promise";
import dotenv from "dotenv";
dotenv.config();

const conn = await mysql.createConnection(process.env.DATABASE_URL);

console.log("=== All tours by status ===");
const [byStatus] = await conn.execute(
  `SELECT status, COUNT(*) AS n FROM tours GROUP BY status ORDER BY n DESC`
);
console.table(byStatus);

console.log("\n=== Tours that have linked departures ===");
const [withDeps] = await conn.execute(
  `SELECT t.status, COUNT(DISTINCT t.id) AS toursWithDeps
   FROM tours t
   INNER JOIN tourDepartures d ON d.tourId = t.id
   GROUP BY t.status`
);
console.table(withDeps);

console.log("\n=== Tours with linked bookings (CANNOT delete safely) ===");
try {
  const [withBookings] = await conn.execute(
    `SELECT COUNT(DISTINCT t.id) AS bookedTours
     FROM tours t
     INNER JOIN bookings b ON b.tourId = t.id`
  );
  console.table(withBookings);
} catch (e) {
  console.log("  bookings table query failed:", e.message);
}

console.log("\n=== Tours created in last 24h vs older ===");
const [byAge] = await conn.execute(
  `SELECT
     CASE WHEN createdAt > NOW() - INTERVAL 24 HOUR THEN 'last_24h' ELSE 'older' END AS age,
     status,
     COUNT(*) AS n
   FROM tours
   GROUP BY age, status
   ORDER BY age, n DESC`
);
console.table(byAge);

console.log("\n=== Active tours by createdBy (admin Jeff vs auto-gen) ===");
const [byCreator] = await conn.execute(
  `SELECT createdBy, isAutoGenerated, COUNT(*) AS n
   FROM tours
   WHERE status = 'active'
   GROUP BY createdBy, isAutoGenerated`
);
console.table(byCreator);

await conn.end();
