// What are the 4 inactive tours?
import mysql from "mysql2/promise";
import dotenv from "dotenv";
dotenv.config();
const conn = await mysql.createConnection(process.env.DATABASE_URL);
const [rows] = await conn.execute(
  `SELECT id, status, calibrationScore, calibrationVerdict,
          destinationCountry, destinationCity,
          LENGTH(description) AS descLen,
          sourceUrl,
          isAutoGenerated, createdBy
   FROM tours
   WHERE status = 'inactive'
   ORDER BY id ASC`
);
console.log("=== Inactive tours ===");
for (const r of rows) {
  console.log(`#${r.id}  cal=${r.calibrationScore}  verdict=${r.calibrationVerdict}  ${r.destinationCountry}/${r.destinationCity}  descLen=${r.descLen}  qa=${r.qaStatus}/${r.qaScore}`);
  console.log(`  sourceUrl: ${r.sourceUrl?.slice(0, 80)}`);
}
await conn.end();
